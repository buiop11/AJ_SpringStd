package com.ahjin.demo.model;

public class CrawlingVO {
	
	
	private String companyName;
	private String ranking;
	private String sales2018;
	private String sales2017;
	private String increase;
	private String form;
	
	
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public String getRanking() {
		return ranking;
	}
	public void setRanking(String ranking) {
		this.ranking = ranking;
	}
	public String getSales2018() {
		return sales2018;
	}
	public void setSales2018(String sales2018) {
		this.sales2018 = sales2018;
	}
	public String getSales2017() {
		return sales2017;
	}
	public void setSales2017(String sales2017) {
		this.sales2017 = sales2017;
	}
	public String getForm() {
		return form;
	}
	public void setForm(String form) {
		this.form = form;
	}
	public String getIncrease() {
		return increase;
	}
	public void setIncrease(String increase) {
		this.increase = increase;
	}
	
	// 그냥 다 String 으로 받음 
	
	
	

}
